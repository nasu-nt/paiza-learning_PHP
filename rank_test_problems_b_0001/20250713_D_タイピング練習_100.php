<?php
// N
// S
// ・ 1 行目には整数 N が与えられます。
// ・ 2 行目には文字列 S が与えられます。
// ・ 入力は合計で 2 行からなり、入力値最終行の末尾に改行が１つ入ります。
// 
// ・ 1 ≦ |S|, N ≦ 100
// ・ |S| は文字列 S の長さ
// ・ S は英小文字からなる文字列
    $N = (int)trim(fgets(STDIN));
    $S = trim(fgets(STDIN));
    for($i=0; $i<(int)$N; $i++)
    {
        echo $S. "\n";
    }

?>